    private void relationshipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_relationshipActionPerformed
        if (relationship.getSelectedIndex() == 1) {
            note.setText("Your choice of PostUTME is for Candidates to be screened, after they apply for KadunaPolytechnic through JAMB. Choose the Relationship correctly, you cannot change it later.");
        }
        else if (relationship.getSelectedIndex() == 2) {
            note.setText("Your choice of APPLICANT is for individuals that wish to purchase application/admission form to apply to the institution. Choose the Relationship correctly, you cannot change it later.");
        }
        else if (relationship.getSelectedIndex() == 3) {
            note.setText("Your choice of NEW STUDENT is for Students that are just admitted into the Institution but not through JAMB. Choose the Relationship correctly, you cannot change it later.");
        }
        else if (relationship.getSelectedIndex() == 4) {
            note.setText("Your choice of RETURNING STUDENT is for Returning/Continuing Students. Choose the Relationship correctly, you cannot change it later.");
        }
        else {
            note.setText("Please select an option.");
        }
    }//GEN-LAST:event_relationshipActionPerformed

