    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        
        if ( firstname.getText() == "" || relationship.getSelectedIndex() == 0 || password.getText() == "" ||  lastname.getText() == "" || state.getText() == "" || mobile.getText() == "" || email.getText() == "") {
            JOptionPane.showMessageDialog(this,"Please fill out all field and try again.");
        }
        else {
            yourfullname.setText("Your fullname is: " + firstname.getText() + " " + lastname.getText());
            yourmobile.setText("Your mobile is: " + mobile.getText());
            youremail.setText("Your email is: " + email.getText());
            yourrelationship.setText("Your relationship is: " + relationship.getSelectedItem().toString());
            yourstate.setText("Your state of orgin is: " + state.getText());
            yourpass.setText(password.getText());
            showDetails.show();
            JOptionPane.showMessageDialog(this,"Record submitted sucessful.");
            
            
            firstname.enable(false);  lastname.setText("");
            mobile.disable();     email.setText("");
            relationship.setSelectedIndex(0);
            state.setText(""); password.setText("");
        }
    }//GEN-LAST:event_submitActionPerformed

    private void resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetActionPerformed
        firstname.setText("");  lastname.setText("");
        mobile.setText("");     email.setText("");
        relationship.setSelectedIndex(0);
        state.setText(""); password.setText("");
        showDetails.hide();
        JOptionPane.showMessageDialog(this,"Application reset sucessful.");
    }//GEN-LAST:event_resetActionPerformed

